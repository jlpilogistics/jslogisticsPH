<?php

namespace App\Http\Controllers;

use App\Vehicle;
use Illuminate\Http\Request;

class VehiclesController extends Controller
{

    public function __construct()
    {
        $this->middleware('auth:admin');
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $vehicles = Vehicle::all();
        return view('admin.fleet.vehicle_table' ,compact('vehicles'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('admin.fleet.vehicle');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $vehicle = new Vehicle;
        $vehicle->vehicle_ref = $request->vehicle_ref;
        $vehicle->plate_no = $request->plate_no;
        $vehicle->date_exp = $request->date_exp;
        $vehicle->date_reg = $request->date_reg;
        $vehicle->vehicle_model = $request->vehicle_model;
        $vehicle->fuel_type = $request->fuel_type;
        $vehicle->avail = $request->avail;
        $vehicle->save();

        return redirect('vehicles');
    }

