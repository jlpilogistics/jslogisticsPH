<script>
    document.getElementById("btnBS").onclick = function () {
        printElement(document.getElementById("printBS"));
    }

    function printElement(elem) {
        var domClone = elem.cloneNode(true);

        var $printSection = document.getElementById("printBs");

        if (!$printSection) {
            var $printSection = document.createElement("div");
            $printSection.id = "printBs";
            document.body.appendChild($printSection);
        }

        $printSection.innerHTML = "";
        $printSection.appendChild(domClone);
        window.print();
    }
</script>